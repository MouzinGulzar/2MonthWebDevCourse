// Conditionals
// less than 18 - not eligible for driving
// greater than 70 - not eligible for driving

let age = 80;

if(age < 18) { //if age is lesser than 18
    console.log("Not eligible for driving 1");
} else if(age > 70) { //if age is greater than 18
    console.log("Not eligible for driving 2");
} else { //if none of the above conditions is true
    console.log("Eligible for driving");
}
 

